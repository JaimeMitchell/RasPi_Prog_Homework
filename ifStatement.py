my_color = input('Please enter color: ')
if (my_color == 'red' or my_color == 'Red' or my_color == 'RED' or my_color == 'r' or my_color == 'R'):
    print('Red is a nice color')
elif (my_color == 'blue' or my_color == 'Blue' or my_color == 'BLUE' or my_color == 'b' or my_color == 'B'):
    print('Blue is a nice color')
elif (my_color == 'green' or my_color == 'Green' or my_color == 'GREEN' or my_color == 'g' or my_color == 'G'):
    print('Green is a nice color')
else:
    print('I do not like that color')